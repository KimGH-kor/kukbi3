package ch03.대출서비스.function;

import ch03.대출서비스.interpace.Lendable;

public class SeparateVolume implements Lendable{

	String requestNo;//청구번호
	String bookTitle;//제목
	String writer;//저자
	String borrower;//대출인
	String checkOutDate;//대출일
	byte state;//대출상태
	
	public SeparateVolume(String requesNo, String bookTitle, String writer) {
		this.requestNo = requesNo;
		this.bookTitle = bookTitle;
		this.writer = writer;
	}
	
	@Override
	public void checkIn(String borrower, String date) {
		if(state != 0) {
			return;
		}
		this.borrower = borrower;
		this.checkOutDate = date;
		this.state = 1;
		System.out.println("*"+bookTitle + "이(가) 대출되었습니다.");
		System.out.println("대출인 : "+borrower);
		System.out.println("대출일 : "+checkOutDate+"\n");
	}

	@Override
	public void checkOut() {
		this.borrower = null;
		this.checkOutDate = null;
		this.state = 0;
		System.out.println("*"+bookTitle+"이(가) 반납되었습니다.\n");
	}

}
